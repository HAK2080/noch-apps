// EXTRACT LESSON (Karpathy pattern)
// ============================================================
export function extractLesson(post, feedback) {
  const type = feedback.status === 'approved' ? 'success' : 'failure'
  const scores = {
    voice: post.score_voice,
    hook: post.score_hook,
    dialect: post.score_dialect,
    humor: post.score_humor,
  }

  const weakest = Object.entries(scores)
    .filter(([, v]) => v != null)
    .sort(([, a], [, b]) => a - b)[0]

  const strongest = Object.entries(scores)
    .filter(([, v]) => v != null)
    .sort(([, a], [, b]) => b - a)[0]

  let lesson = ''
  if (type === 'success') {
    lesson = `Approved post. Strongest dimension: ${strongest?.[0]} (${strongest?.[1]}). `
    lesson += feedback.revision_notes ? `Notes: ${feedback.revision_notes}` : 'Keep this pattern.'
  } else {
    lesson = `${feedback.status === 'rejected' ? 'Rejected' : 'Needs revision'} post. `
    lesson += weakest ? `Weakest: ${weakest[0]} (${weakest[1]}). ` : ''
    lesson += feedback.rejection_reason ? `Reason: ${feedback.rejection_reason}` : ''
    if (post.dialect_corrections) {
      lesson += ` Dialect corrections: ${JSON.stringify(post.dialect_corrections)}`
    }
  }

  return {
    brand_id: post.brand_id,
    experiment_type: weakest ? `${weakest[0]}_test` : 'voice_test',
    hypothesis: `Does improving ${weakest?.[0] || 'voice'} score increase approval rate?`,
    content_post_id: post.id,
    score_before: post.score_total,
    lesson_learned: lesson,
    applied_to_program: false,
  }
}

// ============================================================
// UPDATE BRAND PROGRAM (self-improvement loop)
// ============================================================
export function updateBrandProgram(currentProgram, experiments) {
  if (!experiments || experiments.length === 0) return currentProgram

  const lessons = experiments
    .filter(e => e.lesson_learned)
    .slice(-10) // Last 10 lessons
    .map(e => `- [${new Date(e.created_at).toLocaleDateString()}] ${e.lesson_learned}`)
    .join('\n')

  const approvedCount = experiments.filter(e => e.score_after > e.score_before).length
  const totalCount = experiments.filter(e => e.score_after != null).length
  const winRate = totalCount > 0 ? Math.round((approvedCount / totalCount) * 100) : 0

  // Replace the Self-Improvement Notes section
  const updatedNotes = `## Self-Improvement Notes
Win Rate: ${winRate}% (${approvedCount}/${totalCount} experiments improved score)
Last Updated: ${new Date().toISOString()}

### Recent Lessons
${lessons || 'No experiments run yet. Approve/reject posts to start the learning loop.'}
`

  return currentProgram.replace(
    /## Self-Improvement Notes[\s\S]*$/,
    updatedNotes
  )
}

