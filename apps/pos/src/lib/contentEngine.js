// SCORE CONTENT
// ============================================================
export function scoreContent(post, brand) {
  // Auto-scoring heuristics (human scores override these)
  const scores = {}

  // Voice score — check for banned words
  const bannedWords = ['amazing', 'incredible', 'delicious', 'premium', 'authentic', 'elevate', 'passionate', 'journey']
  const caption = (post.caption_en || '').toLowerCase()
  const bannedFound = bannedWords.filter(w => caption.includes(w)).length
  scores.voice = Math.max(3, 10 - bannedFound * 2)

  // Hook strength — first line length and punctuation
  const firstLine = (post.caption_en || '').split('\n')[0] || ''
  scores.hook = firstLine.length > 10 && firstLine.length < 100 ? 7 : 5

  // Dialect score — check for Libyan Tripoli markers
  const dialectMarkers = ['هسّا', 'بزاف', 'زين', 'أحنا', 'إيه', 'شوية', 'ليش', 'وين', 'بكرة', 'خلاص', 'شو', 'تمام', 'كويس']
  const arabicCaption = post.caption_ar || ''
  const dialectFound = dialectMarkers.filter(m => arabicCaption.includes(m)).length
  scores.dialect = Math.min(10, 5 + dialectFound * 1.5)

  // Humor score — presence of humor markers
  const humorMarkers = ['allegedly', 'we\'re not saying', 'nochi', 'okay but', 'the thing is']
  const humorFound = humorMarkers.filter(m => caption.includes(m)).length
  scores.humor = Math.min(10, 5 + humorFound * 1.5)

  // Relevance — base score, human adjusts
  scores.relevance = 7

  // Weighted total
  const weights = { voice: 0.3, hook: 0.25, dialect: 0.2, humor: 0.15, relevance: 0.1 }
  scores.total = Object.keys(weights).reduce((sum, k) => sum + (scores[k] || 5) * weights[k], 0)

  return scores
}

